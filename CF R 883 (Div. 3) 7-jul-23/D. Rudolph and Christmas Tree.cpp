int solve() {
  // Code aquí
  int n, H;
  lli b;
  vi h;
  long double sol = 0;
  cin >> n >> b >> H;
  h = lee(n);

  for (int i = 1; i < n; i++) {
    sol = sol + b * min(h[i] - h[i - 1], H) -
          b * (pow((long double)min(h[i] - h[i - 1], H), 2) / (2 * H));
  }
  sol = sol + (long double)(b * H) / 2;
  cout << fixed << sol << endl;
  return 0;
}

